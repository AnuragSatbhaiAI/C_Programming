#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : It is used to display the word "Marvellous" five times
//  Input         : None
//  Output        : Display's Marvellous 5 times
//  Author        : Anurag Gopal Satbhai
//  Date          : 25/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void Display()
{
    int i = 0;

    for(i = 1; i <= 5; i++)
    {
        printf("Marvellous\n");
    }

}

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    Display();

    return 0;
}

