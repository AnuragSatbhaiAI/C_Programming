#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : It is used to display 5 to 1 numbers on screen
//  Input         : None
//  Output        : Displays numbers from 5 to 1
//  Author        : Anurag Gopal Satbhai
//  Date          : 25/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void Display()
{
    int i = 0;
    i = 5;
    while(i >= 1)
    {
        printf("%d\n",i);
        i--;
    }

}

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    Display();

    return 0;
}

