#include<stdio.h>
#include<stdbool.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Check
//  Description   : It is used to check whether a number is divisible by 5
//  Input         : int
//  Output        : bool
//  Author        : Anurag Gopal Satbhai
//  Date          : 25/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

bool Check(int iNo)
{
    if (iNo % 5 == 0)                                  // Business Logic
    {
        return true;
    }
    else 
    {
        return false;
    }

}

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;
    bool bRet = false;

    printf("Enter the number : \n");
    scanf("%d",&iValue);

    bRet = Check(iValue);

    if(bRet == true)
    {
        printf("Divisible by 5.");
    }
    else
    {
        printf("Not Divisible by 5.");
    }

    return 0;
}

