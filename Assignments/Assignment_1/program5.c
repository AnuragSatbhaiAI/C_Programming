#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : PrintStar
//  Description   : It is used to print stars on the screen
//  Input         : int
//  Output        : Prints '*' character as many times as entered by the user
//  Author        : Anurag Gopal Satbhai
//  Date          : 25/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void PrintStar(int iNo)
{
    int iCnt = 0;

    for(iCnt = 1; iCnt <= iNo; iCnt++)                      // Business Logic
    {
        printf("*");
    }

}

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;

    printf("Enter the number : \n");
    scanf("%d",&iValue);

    PrintStar(iValue);

    return 0;
}

