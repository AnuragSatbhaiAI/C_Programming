#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CircleArea
//  Description   : It returns the area of the circle
//  Input         : float
//  Output        : double
//  Author        : Anurag Gopal Satbhai
//  Date          : 31/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

double CircleArea(float fRadius)
{
    double dArea = 1.0;

    dArea = 3.14 * fRadius * fRadius;         // Business Logic

    return dArea;

    
}   // Time Complexity = O(1)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    float fValue = 0.0;
    double dRet = 0.0;

    printf("Enter radius : ");
    scanf("%f",&fValue);

    dRet = CircleArea(fValue);

    printf("Area of circle is %1f",dRet);

    return 0;
}

