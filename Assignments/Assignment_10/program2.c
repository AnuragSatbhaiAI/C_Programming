#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : RectArea
//  Description   : It returns the area of rectangel
//  Input         : float
//  Output        : double
//  Author        : Anurag Gopal Satbhai
//  Date          : 31/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

double RectArea(float fWidth, float fHeight)
{
    double dArea = 1.0;

    dArea =  fWidth * fHeight;         // Business Logic

    return dArea;

    
}   // Time Complexity = O(1)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    float fValue1 = 0.0, fValue2 = 0.0;
    double dRet = 0.0;

    printf("Enter Width : ");
    scanf("%f",&fValue1);

    printf("Enter Height : ");
    scanf("%f",&fValue2);

    dRet = RectArea(fValue1, fValue2);

    printf("Area of rectangle is %1f",dRet);

    return 0;
}

