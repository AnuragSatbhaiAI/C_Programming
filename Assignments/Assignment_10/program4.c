/////////////////////////////////////////////////////////////////////////////////////////
//
//  4.Write a program which accept temperature in fahrenheit and convert it into celsius
//    (1 celsius = (fahrenheit - 32) * (5/9))
//
//  Input  : 10                                     
//  Output : -12.2222                                                                          
//
//////////////////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Required header file
//
//////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : FhtoCs
//  Description   : Converts temperature from Fahrenheit to Celsius
//  Input         : float
//  Output        : double
//  Author        : Anurag Gopal Satbhai
//  Date          : 31/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

double FhtoCs(float fTemp)
{

    return ((fTemp - 32.0) * (5.0/9.0));

    
}   // Time Complexity = O(1)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    float fValue = 0.0;
    double dRet = 0.0;

    printf("Enter temperature in fahrenheit : ");
    scanf("%f",&fValue);

    dRet = FhtoCs(fValue);

    printf("Temperature in celsius is %1f",dRet);

    return 0;
}

