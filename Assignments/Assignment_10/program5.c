//  5.Write a program which accept area in square feet and convert it into square meter
//    (1 square feet = 0.0929 square meter)
//
//  Input  : 5                              7                       
//  Output : 0.464515                       0.650321                                                                     
//
//////////////////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Required header file
//
//////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : SquareMeter
//  Description   : Converts Square feet into square meter
//  Input         : int
//  Output        : double
//  Author        : Anurag Gopal Satbhai
//  Date          : 31/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

double SquareMeter(int iFeet)
{

    return (iFeet * 0.0929);             // Business Logic
   

}   // Time Complexity = O(1)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;
    double dRet = 0.0;

    printf("Enter area in square feet : ");
    scanf("%d",&iValue);

    dRet = SquareMeter(iValue);

    printf("Converted Square meter is  %1f",dRet);

    return 0;
}

