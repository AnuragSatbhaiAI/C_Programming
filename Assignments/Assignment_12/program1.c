#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : PrintFactors
//  Description   : Displays factors of that number
//  Input         : int
//  Output        : Prints factors
//  Author        : Anurag Gopal Satbhai
//  Date          : 02/11/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void PrintFactors(int iNo)
{

    int iCnt = 0; 
    
    for(iCnt = 2; iCnt <= (iNo/2); iCnt++)
    {
        if((iNo % iCnt) == 0)
        {
            printf("%d\t", iCnt);
        }
    }  

}   // Time Complexity = O(n/2)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    PrintFactors(iValue);

    return 0;
}

