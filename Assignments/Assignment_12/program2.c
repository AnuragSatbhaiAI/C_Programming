#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CountFactors
//  Description   : Counts factors of that number and retunr
//  Input         : int
//  Output        : int
//  Author        : Anurag Gopal Satbhai
//  Date          : 02/11/25
//
//////////////////////////////////////////////////////////////////////////////////////////

int CountFactors(int iNo)
{

    int iCnt = 0, iTotalFact = 0; 
    
    for(iCnt = 2; iCnt <= (iNo/2); iCnt++)               // Business Logic
    {
        if((iNo % iCnt) == 0)
        {
            iTotalFact = iTotalFact + 1;
        }
    }

    return iTotalFact;

}   // Time Complexity = O(n/2)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0, iRet = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    iRet = CountFactors(iValue);

    printf("Total factor count is %d",iRet);

    return 0;
}

