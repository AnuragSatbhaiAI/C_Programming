#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : SumOfFactors
//  Description   : Calculates and returns the sum of all factors of a given number
//  Input         : int
//  Output        : int
//  Author        : Anurag Gopal Satbhai
//  Date          : 02/11/25
//
//////////////////////////////////////////////////////////////////////////////////////////

int SumOfFactors(int iNo)
{

    int iCnt = 0, iSumFact = 0; 
    
    for(iCnt = 2; iCnt <= (iNo/2); iCnt++)               // Business Logic
    {
        if((iNo % iCnt) == 0)
        {
            iSumFact = iSumFact + iCnt;
        }
    }

    return iSumFact;

}   // Time Complexity = O(n/2)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0, iRet = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    iRet = SumOfFactors(iValue);

    printf("Sum of factors is %d",iRet);

    return 0;
}

