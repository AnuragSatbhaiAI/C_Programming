#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : SumOfEvenFactors
//  Description   : Calculates and returns the sum of even factors of a given number
//  Input         : int
//  Output        : int
//  Author        : Anurag Gopal Satbhai
//  Date          : 02/11/25
//
//////////////////////////////////////////////////////////////////////////////////////////

int SumOfEvenFactors(int iNo)
{

    int iCnt = 0, iSumEvenFact = 0; 
    
    for(iCnt = 2; iCnt <= (iNo/2); iCnt++)               // Business Logic
    {
        if((iNo % iCnt) == 0)
        {
            if((iCnt %2) == 0)
            {
                iSumEvenFact = iSumEvenFact + iCnt;
            }
        }
    }

    return iSumEvenFact;

}   // Time Complexity = O(n/2)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0, iRet = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    iRet = SumOfEvenFactors(iValue);

    printf("Sum of even factors is %d",iRet);

    return 0;
}

