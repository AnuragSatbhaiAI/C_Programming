#include <stdio.h>
#include <stdbool.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : IsDivisibleByFive
//  Description   : Calculates and returns the sum of even factors of a given number
//  Input         : int
//  Output        : int
//  Author        : Anurag Gopal Satbhai
//  Date          : 02/11/25
//
//////////////////////////////////////////////////////////////////////////////////////////

bool IsDivisibleByFive(int iNo)
{
    if((iNo % 5) == 0)
    {
        return true;
    }
    else
    {
        return false;
    }

}   // Time Complexity = O(1)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    printf("%s\n",IsDivisibleByFive(iValue) ? "Yes":"No");

    return 0;
}

