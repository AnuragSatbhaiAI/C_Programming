#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : print_even_numbers
//  Description   : Prints all even numbers between 2 and N
//  Input         : int
//  Output        : Prints even numbers
//  Author        : Anurag Gopal Satbhai
//  Date          : 02/11/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void print_even_numbers(int limit)
{
    int iCnt = 0;

    for(iCnt = 2; iCnt <= limit; iCnt = iCnt + 2)       // Business Logic
    {
        printf("%d ",iCnt);
    }

}   // Time Complexity = O(n/2)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int limit;

    printf("Enter number : ");
    scanf("%d",&limit);

    print_even_numbers(limit);

    return 0;
}

