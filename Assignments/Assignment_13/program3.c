#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : print_odd_numbers
//  Description   : Prints all odd numbers between 1 and N
//  Input         : int
//  Output        : Prints odd numbers
//  Author        : Anurag Gopal Satbhai
//  Date          : 02/11/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void print_odd_numbers(int limit)
{
    int iCnt = 0;

    for(iCnt = 1; iCnt <= limit; iCnt = iCnt + 2)
    {
        printf("%d ",iCnt);
    }

}   // Time Complexity = O(n/2)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int limit;

    printf("Enter number : ");
    scanf("%d",&limit);

    print_odd_numbers(limit);

    return 0;
}

