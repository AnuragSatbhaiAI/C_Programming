//////////////////////////////////////////////////////////////////////////////////////////
//
//  Required header file
//
//////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : sum_natural_numbers
//  Description   : Returns sum of natural numbers up to N.
//  Input         : int
//  Output        : int
//  Author        : Anurag Gopal Satbhai
//  Date          : 02/11/25
//
//////////////////////////////////////////////////////////////////////////////////////////

int sum_natural_numbers(int limit)
{
    int iCnt = 0, iSum = 0;

    for(iCnt = 1; iCnt <= limit; iCnt++)
    {
        iSum = iSum + iCnt;
    }

    return iSum;

}   // Time Complexity = O(n)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int limit;

    printf("Enter number : ");
    scanf("%d",&limit);

    printf("%d \n",sum_natural_numbers(limit));

    return 0;
}

