//////////////////////////////////////////////////////////////////////////////////////////
//
//  Required header file
//
//////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : sum_even_numbers
//  Description   : Returns the sum of all even numbers up to N.
//  Input         : int
//  Output        : int
//  Author        : Anurag Gopal Satbhai
//  Date          : 02/11/25
//
//////////////////////////////////////////////////////////////////////////////////////////

int sum_even_numbers(int limit)
{
    int iCnt = 0, iSum = 0;

    for(iCnt = 2; iCnt <= limit; iCnt = iCnt + 2)
    {
        iSum = iSum + iCnt;
    }

    return iSum;

}   // Time Complexity = O(n/2)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int limit;

    printf("Enter number : ");
    scanf("%d",&limit);

    printf("%d \n",sum_even_numbers(limit));

    return 0;
}

