#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : DisplayDigits
//  Description   : Displays each digit of the given number in reverse order.
//  Input         : int
//  Output        : Display digits
//  Author        : Anurag Gopal Satbhai
//  Date          : 02/11/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void DisplayDigits(int iNo)
{
    int iDigit = 0;

    if(iNo < 0)
    {
        iNo = -iNo;
    }

    while(iNo > 0)                                // Business Logic
    {
        iDigit = iNo % 10 ;
        printf("%d\n", iDigit);

        iNo = iNo/10;
    }

}   // Time Complexity = O(digits)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    DisplayDigits(iValue);

    return 0;
}

