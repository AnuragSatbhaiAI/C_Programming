#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CountDiff
//  Description   : Returns the difference between summation of even and odd digits.
//  Input         : int
//  Output        : int
//  Author        : Anurag Gopal Satbhai
//  Date          : 02/11/25
//
//////////////////////////////////////////////////////////////////////////////////////////

int CountDiff(int iNo)
{
    int iEven = 0, iOdd = 0;
    int iDigit = 0;

    if(iNo < 0)
    {
        iNo = -iNo;
    }

    while(iNo > 0)
    {
        iDigit = iNo % 10;

        if((iDigit %2) == 0)
        {
            iEven = iEven + iDigit;
        }
        else
        {
            iOdd = iOdd + iDigit;
        }
    
        iNo = iNo / 10;
    }

    return iEven - iOdd;


}   // Time Complexity = O(digits)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0, iRet = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    iRet = CountDiff(iValue);

    printf("%d",iRet);

    return 0;
}

