#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : It is used to print stars on the screen
//  Input         : int
//  Output        : Prints '*' character as many times as entered by the user
//  Author        : Anurag Gopal Satbhai
//  Date          : 26/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    int iCnt = 0;

    if(iNo < 0)                                            // Updater
    {
        iNo = -iNo;
    }

    iCnt = 1;
    while(iCnt <= iNo)                                     // Business Logic
    {
        printf("*");
        iCnt++;
    }

}

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;

    printf("Enter the number : \n");
    scanf("%d",&iValue);

    Display(iValue);

    return 0;
}

