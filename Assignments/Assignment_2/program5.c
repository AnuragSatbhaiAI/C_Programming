bool ChkEven(int iNo)
{
   if(iNo % 2 == 0)                           // Business Logic.
   {
    return true;
   }
   else 
   {
    return false;
   }

}

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;
    bool bRet = false;

    printf("Enter number : ");
    scanf("%d",&iValue);

    bRet = ChkEven(iValue);

    if(bRet == true)
    {
        printf("%d is Even.",iValue);
    }
    else
    {
        printf("%d is Odd.",iValue);
    }

    return 0;
}

