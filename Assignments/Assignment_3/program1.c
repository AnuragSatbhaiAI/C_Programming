#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : PrintEven
//  Description   : It is used to print even numbers.
//  Input         : int
//  Output        : Even numbers
//  Author        : Anurag Gopal Satbhai
//  Date          : 26/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void PrintEven(int iNo)
{
   if(iNo < 0)
   {
    return;
   }
   
   int iCnt = 0;
   for(iCnt = 1; iCnt <= (iNo * 2); iCnt++)
   {
    if ((iCnt % 2) == 0)
    {
        printf("%d\t",iCnt);
    }
   }

}

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    PrintEven(iValue);

    return 0;
}

