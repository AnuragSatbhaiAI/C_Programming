#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : DisplayFactor
//  Description   : It is used to print even factors.
//  Input         : int
//  Output        : Even factors
//  Author        : Anurag Gopal Satbhai
//  Date          : 26/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void DisplayFactor(int iNo)
{
   if(iNo < 0)
   {
        iNo = -iNo;
   }
   
   int i = 0;
   for(i = 1; i <= (iNo / 2); i++)
   {
    if ((iNo % i) == 0)
    {
        printf("%d\t",i);
    }
   }

}

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    DisplayFactor(iValue);

    return 0;
}

