#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : DisplayConvert
//  Description   : It is used to convert the case of an entered character.
//  Input         : char
//  Output        : char
//  Author        : Anurag Gopal Satbhai
//  Date          : 26/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void DisplayCnovert(char cValue)
{
   if(cValue >= 'A' && cValue <= 'Z')
   {
        printf("%c", cValue + 32);
   }
   else if(cValue >= 'a' && cValue <= 'z')
   {
        printf("%c",cValue - 32);
   }

}

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    char cValue = '\0';

    printf("Enter Character : ");
    scanf("%c",&cValue);

    DisplayCnovert(cValue);

    return 0;
}

