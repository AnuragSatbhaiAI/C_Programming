//
//  2.Accept character from user and check whether it is capital or not (A-Z).
//
//    Input  : F        d
//
//    Output : True    False
//
//////////////////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Required header file
//
//////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>

# define TRUE 1
# define FALSE 0
typedef int BOOL;


//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : ChkCapital
//  Description   : Checks the given element is capital letter or not.
//  Input         : char
//  Output        : BOOL
//  Author        : Anurag Gopal Satbhai
//  Date          : 07/12/25
//
//////////////////////////////////////////////////////////////////////////////////////////

BOOL ChkCapital(char ch)
{
    
    if(ch >= 'A' && ch<= 'Z')
    {
        return TRUE;
    }

    return FALSE;
    

}        

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';
    BOOL bRet = FALSE;

    printf("Enter the character : \n");
    scanf("%c",&cValue);
    
    bRet = ChkCapital(cValue);

    if(bRet == TRUE)
    {
        printf("It is capital character.\n");
    }
    else
    {
        printf("It is not a capital character.\n");
    }

    return 0;
}

