//
//  3.Accept character from user and check whether it is digit or not (0-9).
//
//    Input  : 7        d
//
//    Output : True    False
//
//////////////////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Required header file
//
//////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>

# define TRUE 1
# define FALSE 0
typedef int BOOL;


//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : ChkDigit
//  Description   : Checks the given element is digit or not.
//  Input         : char
//  Output        : BOOL
//  Author        : Anurag Gopal Satbhai
//  Date          : 07/12/25
//
//////////////////////////////////////////////////////////////////////////////////////////

BOOL ChkDigit(char ch)
{
    
    if(ch >= '0' && ch<= '9')
    {
        return TRUE;
    }

    return FALSE;
    

}        

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';
    BOOL bRet = FALSE;

    printf("Enter the character : \n");
    scanf("%c",&cValue);
    
    bRet = ChkDigit(cValue);

    if(bRet == TRUE)
    {
        printf("It is a digit.\n");
    }
    else
    {
        printf("It is not a digit.\n");
    }

    return 0;
}

