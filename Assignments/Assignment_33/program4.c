//
//  4.Accept character from user and check whether it is small case or not (a-z).
//
//    Input  : g        D
//
//    Output : True    False
//
//////////////////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Required header file
//
//////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>

# define TRUE 1
# define FALSE 0
typedef int BOOL;


//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : ChkSmall
//  Description   : Checks the given element is small case or not.
//  Input         : char
//  Output        : BOOL
//  Author        : Anurag Gopal Satbhai
//  Date          : 07/12/25
//
//////////////////////////////////////////////////////////////////////////////////////////

BOOL ChkSmall(char ch)
{
    
    if(ch >= 'a' && ch <= 'z')
    {
        return TRUE;
    }

    return FALSE;
    

}        

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';
    BOOL bRet = FALSE;

    printf("Enter the character : \n");
    scanf("%c",&cValue);
    
    bRet = ChkSmall(cValue);

    if(bRet == TRUE)
    {
        printf("It is a small case character.\n");
    }
    else
    {
        printf("It is not a small case character.\n");
    }

    return 0;
}

