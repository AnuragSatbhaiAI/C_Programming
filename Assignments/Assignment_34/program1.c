void DisplayASCII()
{
    int iCnt = 0;

    printf("---------------------------------------------------------------------\n");
    printf("    Character   Decimal   Octal    Hexadecimal\n");
    printf("---------------------------------------------------------------------\n");

    for(iCnt = 0; iCnt <= 255; iCnt++)
    {
        printf("       %c       %3d       %03o        %02X      \n", iCnt,iCnt,iCnt,iCnt);
    }

    printf("---------------------------------------------------------------------\n");

}        

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';
    
    DisplayASCII();

    return 0;
}


