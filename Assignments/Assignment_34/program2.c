#include <stdio.h>


//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Converts char to upper - lower and lower - upper.
//  Input         : char
//  Output        : NA
//  Author        : Anurag Gopal Satbhai
//  Date          : 07/12/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void Display(char ch)
{
    
    if((ch >= 'a') && (ch <= 'z'))
    {
        printf("%c",(ch - 32) );
    }
    else if ((ch >= 'A') && (ch <= 'Z'))
    {
        printf("%c",(ch + 32));
    }
    else
    {
        printf("%c",ch);
    }

}        

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';

    printf("Enter your character : \n");
    scanf("%c",&cValue);
    
    Display(cValue);

    return 0;
}

