#include <stdio.h>


//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Display the alphabest till end .
//  Input         : char
//  Output        : NA
//  Author        : Anurag Gopal Satbhai
//  Date          : 07/12/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void Display(char ch)
{
    
    if((ch >= 'a') && (ch <= 'z'))
    {
        while(ch != 'a')
        {
            printf("%c\t",ch);
            ch--;
        }
        printf("%c\t",ch);
    }
    else if ((ch >= 'A') && (ch <= 'Z'))
    {
        while(ch != 'Z')
        {
            printf("%c\t",ch);
            ch++;
        }
        printf("%c\t",ch);
    }
    else
    {
        return;
    }

}        

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';

    printf("Enter your character : \n");
    scanf("%c",&cValue);
    
    Display(cValue);

    return 0;
}

