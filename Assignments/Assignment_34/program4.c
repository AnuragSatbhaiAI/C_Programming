#include <stdio.h>
#include <stdbool.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : ChkSpecial
//  Description   : Function checks the input is special character or not.
//  Input         : char
//  Output        : bool
//  Author        : Anurag Gopal Satbhai
//  Date          : 07/12/25
//
//////////////////////////////////////////////////////////////////////////////////////////

bool ChkSpecial(char ch)
{
    
    if((ch == '!') || (ch == '@') || (ch == '#') || (ch == '$') || 
    (ch == '%') || (ch == '^') || (ch == '&') || (ch == '*'))
    {
        return true;
    }
    else
    {
        return false;
    }

}        

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';
    bool bRet = false;

    printf("Enter your character : \n");
    scanf("%c",&cValue);
    
    bRet = ChkSpecial(cValue);

    if(bRet == true)
    {
        printf("It is a special character.");
    }
    else
    {
        printf("It is not a special character.");
    }

    return 0;
}

