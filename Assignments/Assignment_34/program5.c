//  5.Accept character from user and display it ASCII value in decimal, octal and 
//    hexadecimal format.
//
//    Input  : A       
//
//    Output : Decimal      65
//             Octal        0101
//             Hexadecimal  0x41
// 
//////////////////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Required header file
//
//////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>
#include <stdbool.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Function displays ASCII values.
//  Input         : char
//  Output        : NA
//  Author        : Anurag Gopal Satbhai
//  Date          : 07/12/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void Display(char ch)
{
    unsigned int ich = (unsigned char)ch;
    
    printf("Decimal     : %d\n",ich);
    printf("Octal       : 0%o\n",ich);
    printf("Hexadecimal : 0x%X\n",ich);

}        

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';

    printf("Enter your character : \n");
    scanf("%c",&cValue);
    
    Display(cValue);

    return 0;
}

