int CountCapital(char *str)
{
    
    int iCount = 0;

    while(*str != '\0')
    {
        if((*str >= 'A') && (*str <= 'Z'))
        {
            iCount++;
        }
        str++;
    }

    return iCount;

}        

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char arr[20];
    int iRet = 0;

    printf("Enter your string : \n");
    scanf("%[^\n]s",arr);

    iRet = CountCapital(arr);

    printf("%d",iRet);

    return 0;
}

