int Difference(char *str)
{
    
    int iCountSmall = 0;
    int iCountCapital = 0;

    while(*str != '\0')
    {
        if((*str >= 'a') && (*str <= 'z'))
        {
            iCountSmall++;
        }
        else if((*str >= 'A') && (*str <= 'Z'))
        {
            iCountCapital++;
        } 
        str++;

    }
    
    return (iCountSmall - iCountCapital);

}        

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char arr[20];
    int iRet = 0;

    printf("Enter your string : \n");
    scanf("%[^\n]s",arr);

    iRet = Difference(arr);

    printf("%d",iRet);

    return 0;
}

