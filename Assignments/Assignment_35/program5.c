void Reverse(char *str)
{

    char *start = str;
    char *end = str;
    char temp = '\0';

    while(*end != '\0')
    {
        end++;
    }
    end--;


    while(start < end)
    {
        temp = *start;
        *start = *end;
        *end = temp;

        start++;
        end--;

    }

    while(*str != '\0')
    {
        printf("%c",*str);
        str++;
    }
    
}        

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char arr[20];

    printf("Enter your string : \n");
    scanf("%[^\n]s",arr);

    Reverse(arr);

    return 0;
}

