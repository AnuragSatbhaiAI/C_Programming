void StrlwrX(char *str)
{

    while(*str != '\0')
    {
        if((*str >= 'A') && (*str <= 'Z'))
        {
            *str = *str + 32;
            printf("%c",*str);
        }
        else
        {
            printf("%c",*str);
        }
        str++;
    }
    
}        

