void StrtoggleX(char *str)
{

    while(*str != '\0')
    {
        if((*str >= 'a') && (*str <= 'z'))
        {
            *str = *str - 32;
        }
        else if((*str >= 'A') && (*str <= 'Z'))
        {
            *str = *str + 32;
        }
        str++;
    }
    
}        

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char arr[20];

    printf("Enter your string : \n");
    scanf("%[^\n]s",arr);

    StrtoggleX(arr);

    printf("Modified string is %s ",arr);

    return 0;
}

