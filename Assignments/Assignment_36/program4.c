void DisplayDigits(char *str)
{

    while(*str != '\0')
    {
        if((*str >= '0') && (*str <= '9'))
        {
            printf("%c",*str);
        }
        str++;
    }
    
}        

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char arr[20];

    printf("Enter your string : \n");
    scanf("%[^\n]s",arr);

    DisplayDigits(arr);

    return 0;
}

