//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : DisplayDigits
//  Description   : Function displays the digits in the string.
//  Input         : str
//  Output        : Na
//  Author        : Anurag Gopal Satbhai
//  Date          : 07/12/25
//
