//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CountWhite
//  Description   : Function counts the number of whitespaces.
//  Input         : str
//  Output        : int
//  Author        : Anurag Gopal Satbhai
//  Date          : 07/12/25
