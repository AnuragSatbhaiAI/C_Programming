#include <stdio.h>

#define TRUE 1
#define FALSE 0
typedef int BOOL;

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : ChkChar
//  Description   : Function checks the given char is present in the string or not.
//  Input         : str, char
//  Output        : BOOL
//  Author        : Anurag Gopal Satbhai
//  Date          : 07/12/25
//
//////////////////////////////////////////////////////////////////////////////////////////

BOOL ChkChar(char *str, char ch)
{

    while(*str != '\0')
    {
        if((*str == ch))
        {
            return TRUE;
        }
        str++;
    }

    return FALSE;
    
}        

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char arr[20];
    char cValue = '\0';
    BOOL bRet = FALSE;

    printf("Enter your string : \n");
    scanf("%[^\n]s",arr);

    printf("Enter the character : \n");
    scanf(" %c",&cValue);

    bRet = ChkChar(arr,cValue);

    if(bRet == TRUE)
    {
        printf("Character found.");
    }
    else
    {
        printf("Character not fournd.");
    }

    return 0;
}

