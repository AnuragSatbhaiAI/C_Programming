//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CountChar
//  Description   : Function count the frequency of the given char.
//  Input         : str, char
//  Output        : int
//  Author        : Anurag Gopal Satbhai
//  Date          : 07/12/25
//
