#include <stdio.h>


//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : LastChar
//  Description   : Function returns the index of the last occurrances of char.
//  Input         : str, char
//  Output        : int
//  Author        : Anurag Gopal Satbhai
//  Date          : 07/12/25
//
