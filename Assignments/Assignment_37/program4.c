int LastChar(char *str, char ch)
{
    int iIndex = 0;
    int iLast = -1;

    while(str[iIndex] != '\0')
    {
        
        if((str[iIndex] == ch))
        {
            iLast = iIndex;
        }
        iIndex++;
    }

    return iLast;
    
}        

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char arr[20];
    char cValue = '\0';
    int iRet = 0;

    printf("Enter your string : \n");
    scanf("%[^\n]s",arr);

    printf("Enter the character : \n");
    scanf(" %c",&cValue);

    iRet = LastChar(arr,cValue);

    printf("Character location is %d",iRet);

    return 0;
}

