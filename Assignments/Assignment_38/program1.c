void StrCpyRevX(char *str, char *dest)
{
    int iLen = 0;
    char *start = str;

    while(*str != '\0')
    {
        iLen++;
        str++;
    }
    iLen--;

    int i = 0;

    while(iLen >= 0)
    {
        dest[i] = start[iLen];

        str--;
        iLen--;
        i++;
    }
    dest[i] = '\0';
}        

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char arr[30];
    char brr[30];

    printf("Enter your string : \n");
    scanf("%[^\n]s",arr);

    StrCpyRevX(arr,brr);

    printf("Reversed copy string is %s",brr);

    return 0;
}

