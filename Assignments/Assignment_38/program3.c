void StrCpyCapX(char *str, char *dest)
{
    
    while(*str != '\0')
    {
        if(*str >= 'a' && *str <= 'z')
        {
            *dest = *str - 32;
        }
        else
        {
            *dest = *str;
        }

        str++;
        dest++;

    }
    *dest = '\0';
}        

