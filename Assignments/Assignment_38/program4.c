//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char arr[30];
    char brr[30];

    printf("Enter your string : \n");
    scanf("%[^\n]s",arr);

    StrCpySmallX(arr,brr);

    printf("Small string is %s",brr);

    return 0;
}

