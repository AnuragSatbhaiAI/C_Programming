void StrCpySmallX(char *str, char *dest)
{
    
    while(*str != '\0')
    {
        if(*str >= 'A' && *str <= 'Z')
        {
            *dest = *str + 32;
        }
        else
        {
            *dest = *str;
        }

        str++;
        dest++;

    }
    *dest = '\0';
}        

