void StrCpyToggleX(char *str, char *dest)
{
    
    while(*str != '\0')
    {
        if(*str >= 'A' && *str <= 'Z')
        {
            *dest = *str + 32;
        }
        else if(*str >= 'a' && *str <= 'z')
        {
            *dest = *str - 32;
        }
        else
        {
            *dest = *str;
        }

        str++;
        dest++;

    }
    *dest = '\0';
}        

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char arr[30];
    char brr[30];

    printf("Enter your string : \n");
    scanf("%[^\n]s",arr);

    StrCpyToggleX(arr,brr);

    printf("Toggled string is %s",brr);

    return 0;
}

