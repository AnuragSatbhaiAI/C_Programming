void Display()
{
    static int iCount = 0;
    if(iCount == 5)
    {
        return;
        iCount = 0;
    }
    iCount++;
    printf("*\t");
    Display();
    
}        

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;
}

