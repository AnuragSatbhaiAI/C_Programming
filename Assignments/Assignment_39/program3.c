#include <stdio.h>


//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Function displays pattern using recurssion.
//  Input         : Na
//  Output        : Na
//  Author        : Anurag Gopal Satbhai
//  Date          : 15/12/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void Display()
{
    static int iCount = 5;
    if(iCount == 0)
    {
        return;
        iCount = 0;
    }
    printf("%d\t",iCount);
    iCount--;
    Display();
    
}        

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;
}

