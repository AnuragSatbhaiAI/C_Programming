void Display()
{
    static int iCount = 0;
    static char cChar = 'A';
    if(iCount == 6)
    {
        iCount = 0;
        cChar = 'A';
        return;
    }
    printf("%c\t",cChar);
    iCount++;
    cChar++;

    Display();
    
}        

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;
}

