//////////////////////////////////////////////////////////////////////////////////////////
//
//  1. Write a program which accept number from user and display its multiplication of
//     factors.
//
//  Input  : 12
//  Output : 144         (1 * 2 * 3 * 4 * 6)
//
//////////////////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Required header file
//
//////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : MultFact
//  Description   : It calculates the multiplication of all factors of the entered number.
//  Input         : int
//  Output        : int
//  Author        : Anurag Gopal Satbhai
//  Date          : 27/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

int MultFact(int iNo)
{

    int iCnt = 0;
    int iMult = 1;

    for(iCnt = 1; iCnt < iNo; iCnt++)               // Business Logic
    {
        if((iNo % iCnt) == 0)
        {
            iMult = iMult * iCnt;
        }
    }

    return iMult;

}       // Time Complexity = O(n)

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;
    int iRet = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    iRet = MultFact(iValue);

    printf("%d",iRet);

    return 0;
}

