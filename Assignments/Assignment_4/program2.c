#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : FactRev
//  Description   : It displays the factors of a number in reverse order.
//  Input         : int
//  Output        : Displays factors
//  Author        : Anurag Gopal Satbhai
//  Date          : 27/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void FactRev(int iNo)
{

    if(iNo < 0)
    {
        iNo = -iNo;
    }

    int iCnt = iNo/2;

    for(iCnt; iCnt > 0; iCnt--)               // Business Logic
    {
        if((iNo % iCnt) == 0)
        {
            printf("%d\t",iCnt);
        }
    }


}       // Time Complexity = O(n/2)

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    FactRev(iValue);

    return 0;
}

