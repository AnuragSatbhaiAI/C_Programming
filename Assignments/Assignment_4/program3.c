#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : NonFact
//  Description   : It displays the non-factor numbers of the given input number.
//  Input         : int
//  Output        : Displays non factors
//  Author        : Anurag Gopal Satbhai
//  Date          : 27/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void NonFact(int iNo)
{

    if(iNo < 0)                                    // Updator
    {
        iNo = -iNo;
    }

    int iCnt = 0;

    for(iCnt = 1; iCnt < iNo; iCnt++)               // Business Logic
    {
        if((iNo % iCnt) != 0)
        {
            printf("%d\t",iCnt);
        }
    }


}       // Time Complexity = O(n)

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    NonFact(iValue);

    return 0;
}

