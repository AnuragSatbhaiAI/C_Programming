//  Required header file
//
//////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : SumNonFact
//  Description   : It displays the non-factor numbers Sum of the given input number.
//  Input         : int
//  Output        : Displays non factors sum
//  Author        : Anurag Gopal Satbhai
//  Date          : 27/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

int SumNonFact(int iNo)
{

    int iCnt = 0;
    int iSum = 0;

    if(iNo < 0)                                    // Updator
    {
        iNo = -iNo;
    }


    for(iCnt = 1; iCnt < iNo; iCnt++)               // Business Logic
    {
        if((iNo % iCnt) != 0)
        {
            iSum = iSum + iCnt;
        }
    }

    return iSum;


}       // Time Complexity = O(n)

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;
    int iRet = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    iRet = SumNonFact(iValue);

    printf("%d",iRet);

    return 0;
}

