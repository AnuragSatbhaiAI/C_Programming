#include <stdio.h>


//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Function displays pattern using recurssion.
//  Input         : int
//  Output        : Na
//  Author        : Anurag Gopal Satbhai
//  Date          : 15/12/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    static char cAlpha = 'A';
    if(iNo == 0)
    {
        cAlpha = "A";
        return;
    }
    printf("%c\t",cAlpha);
    iNo--;
    cAlpha++;
    Display(iNo);
    
}        

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number : \n");
    scanf("%d",&iValue);

    Display(iValue);

    return 0;
}

