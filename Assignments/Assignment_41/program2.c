//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Sum
//  Description   : Function accept number and return the sum of the digits.
//  Input         : int
//  Output        : int
//  Author        : Anurag Gopal Satbhai
//  Date          : 15/12/25
//
//////////////////////////////////////////////////////////////////////////////////////////


int  Sum(int iNo)
{
    
    if(iNo == 0)
    {
        return 0;
    }
    
    return ((iNo % 10) + Sum(iNo / 10));
    
}  
       

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter number : \n");
    scanf("%d",&iValue);

    iRet = Sum(iValue);

    printf("%d",iRet);

    return 0;
}

