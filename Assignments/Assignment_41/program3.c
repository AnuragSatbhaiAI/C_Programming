int Strlen(char *str)
{
    static int i = 0;
    if(str[i] == '\0')
    {
        return i;
    }
    i++;
    
    Strlen(str);
    
}  
       

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iRet = 0;
    char arr[20];

    printf("Enter string : \n");
    scanf("%s",arr);

    iRet = Strlen(arr);

    printf("%d",iRet);

    return 0;
}

