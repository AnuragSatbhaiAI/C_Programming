//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Fact
//  Description   : Function returns factorial of number.
//  Input         : str
//  Output        : int
//  Author        : Anurag Gopal Satbhai
//  Date          : 15/12/25
