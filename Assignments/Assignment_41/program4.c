int Fact(int iNo)
{
    static int iFact = 1;
    if(iNo == 1)
    {
        return 1;
    }

    return iNo * Fact(iNo - 1);
}  
       

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iRet = 0;
    int iValue = 0;

    printf("Enter number : \n");
    scanf("%d",&iValue);

    iRet = Fact(iValue);

    printf("%d",iRet);

    return 0;
}

