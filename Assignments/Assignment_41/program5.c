#include <stdio.h>


//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Mult
//  Description   : Function accept number and return the multiplication of the digits.
//  Input         : int
//  Output        : int
//  Author        : Anurag Gopal Satbhai
//  Date          : 15/12/25
//
//////////////////////////////////////////////////////////////////////////////////////////


int  Mult(int iNo)
{
    
    if(iNo == 0)
    {
        return 1;
    }
    
    return ((iNo % 10) * Mult(iNo / 10));
    
}  
       

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter number : \n");
    scanf("%d",&iValue);

    iRet = Mult(iValue);

    printf("%d",iRet);

    return 0;
}

