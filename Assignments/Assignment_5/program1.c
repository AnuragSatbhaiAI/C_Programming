#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CheckEvenOdd
//  Description   : It checks even or odd number.
//  Input         : None
//  Output        : prints number is even or odd
//  Author        : Anurag Gopal Satbhai
//  Date          : 28/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void CheckEvenOdd(int num)
{

    if((num % 2) == 0)
    {
        printf("%d is Even number.",num);
    }
    else
    {
        printf("%d is Odd number.",num);
    }

}      

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    CheckEvenOdd(iValue);

    return 0;
}

