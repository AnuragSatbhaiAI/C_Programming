#include <stdio.h>
#include <stdbool.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : ChkGreater
//  Description   : Checks the number is greater than 100 or not
//  Input         : int
//  Output        : bool
//  Author        : Anurag Gopal Satbhai
//  Date          : 29/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

bool ChkGreater(int iNo)
{
 
    if(iNo > 100)                          // Business Logic
    {
        return true;
    }
    else
    {
        return false;
    }

}      

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;
    bool bRet = false;

    printf("Please enter number : ");
    scanf("%d",&iValue);

    bRet = ChkGreater(iValue);

    if(bRet == true)
    {
        printf("Greater");
    }
    else
    {
        printf("Smaller");
    }

    return 0;
}

