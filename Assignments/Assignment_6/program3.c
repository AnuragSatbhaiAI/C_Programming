#include <stdio.h>
#include <stdbool.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : ChkEqual
//  Description   : Checks the numbers are equal or not
//  Input         : int, int
//  Output        : bool
//  Author        : Anurag Gopal Satbhai
//  Date          : 29/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

bool ChkEqual(int iNo1,int iNo2)
{
 
    if(iNo1 == iNo2)                          // Business Logic
    {
        return true;
    }
    else
    {
        return false;
    }

}      

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue1 = 0, iValue2 = 0;
    bool bRet = false;

    printf("Please enter two numbers : ");
    scanf("%d %d",&iValue1, &iValue2);

    bRet = ChkEqual(iValue1,iValue2);

    if(bRet == true)
    {
        printf("Equal");
    }
    else
    {
        printf("Not Equal");
    }

    return 0;
}

