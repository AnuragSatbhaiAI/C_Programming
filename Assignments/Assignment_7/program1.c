//
//  1.Write a program which accept number from user and print that number of $ and * on 
//    screen.
//
//  Input  : 5
//  Output : $  *   $   *   $   *   $   *   $   *                                                               
//
//////////////////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Required header file
//
//////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Pattern
//  Description   : Prints $ and * pattern
//  Input         : int
//  Output        : Pattern
//  Author        : Anurag Gopal Satbhai
//  Date          : 31/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void Pattern(int iNo)
{

    int iCnt = 0;

    if(iNo < 0)                                 // Updater
    {
        iNo = -iNo;
    }

    for(iCnt = 1; iCnt <= iNo; iCnt++)              // Business Logic
    {
        printf("$\t*\t");
    }

}   // Time Complexity = O(n)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    Pattern(iValue);

    return 0;
}

