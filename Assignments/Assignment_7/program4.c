//  4.Write a program which accepts N from user and print all odd numbers up to N
//
//  Input  : 18
//  Output : 1  3   5   7   9   11  13  15  17                                                               
//
//////////////////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Required header file
//
//////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : OddDisplay
//  Description   : Prints the all odd numbers up to entered number
//  Input         : int
//  Output        : Prints odd numbers
//  Author        : Anurag Gopal Satbhai
//  Date          : 31/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void OddDisplay(int iNo)
{

    int iCnt = 0;

    if(iNo < 0)                                 // Updater
    {
        iNo = -iNo;
    }

    for(iCnt = 1; iCnt <= iNo; iCnt++)              // Business Logic
    {
        if((iCnt % 2) != 0)
        {
        printf("%d \t", iCnt);
        }
    }

}   // Time Complexity = O(n)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    OddDisplay(iValue);

    return 0;
}

