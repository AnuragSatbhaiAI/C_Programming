//  5.Write a program which accepts N & print first 5 Multiples of N.
//
//  Input  : 4
//  Output : 4  8   12  16  20                                                               
//
//////////////////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Required header file
//
//////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : MultipleDisplay
//  Description   : Prints all multiples of the entered number up to 5
//  Input         : int
//  Output        : Prints multiples
//  Author        : Anurag Gopal Satbhai
//  Date          : 31/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void MultipleDisplay(int iNo)
{

    int iCnt = 0;

    if(iNo < 0)                                 // Updater
    {
        iNo = -iNo;
    }

    // Static loop
    for(iCnt = 1; iCnt <= 5; iCnt++)              // Business Logic
    {
    
        printf("%d \t", iCnt * iNo);
        
    }

}   // Time Complexity = O(1)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    MultipleDisplay(iValue);

    return 0;
}

