//
//  1.Write a program which accept number from user and if number is less than 50 then 
//    print small, if it is greater than 50 and less than 100 print medium, if it is
//    greater than 100 then print large.
//
//  Input  : 75
//  Output : Medium                                                               
//
//////////////////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Required header file
//
//////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Number
//  Description   : Determines whether the number is small, medium, or large
//  Input         : int
//  Output        : Prints Small / Medium / Large
//  Author        : Anurag Gopal Satbhai
//  Date          : 31/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void Number(int iNo)
{

    if(iNo < 50)
    {
        printf("Small");
    }

    else if((iNo >= 50) && (iNo <= 100))
    {
        printf("Medium");
    }
    else
    {
        printf("Large");
    }

}   // Time Complexity = O(1)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    Number(iValue);

    return 0;
}

