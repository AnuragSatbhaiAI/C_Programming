//  Input  : 5                                     -5
//  Output : 120 (5 * 4 * 3 * 2 * 1)                120 (5 * 4 * 3 * 2 * 1)                                                
//
//////////////////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Required header file
//
//////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Factorial
//  Description   : It gives the factorial of the given number.
//  Input         : int
//  Output        : Returns factorial
//  Author        : Anurag Gopal Satbhai
//  Date          : 31/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

int Factorial(int iNo)
{

    if(iNo < 0)                                  // Updater
    {
        iNo = - iNo;
    }

    int iCnt = 0;
    int iFact = 1;

    for(iCnt = iNo; iCnt >= 1; iCnt--)           // Business Logic
    {
        iFact = iFact * iCnt;
    }

    return iFact;
    
}   // Time Complexity = O(n)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0, iRet = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    iRet = Factorial(iValue);

    printf("%d",iRet);

    return 0;
}

