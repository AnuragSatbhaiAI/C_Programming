#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Table
//  Description   : Prints the multiplication table of the given number.
//  Input         : int
//  Output        : Prints Table
//  Author        : Anurag Gopal Satbhai
//  Date          : 31/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void Table(int iNo)
{

    if(iNo < 0)                                  // Updater
    {
        iNo = - iNo;
    }

    if(iNo == 0)
    {
        printf("Table of 0 is always 0.\n");
        return;
    }

    int iCnt = 0;

    for(iCnt = 1; iCnt <= 10; iCnt++)           // Business Logic
    {
        printf("%d\t",iCnt * iNo);
    }

    
}   // Time Complexity = O(n)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    Table(iValue);

    return 0;
}

