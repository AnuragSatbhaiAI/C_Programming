#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : TableRev
//  Description   : It gives the Table of the given number in reverse order.
//  Input         : int
//  Output        : Prints Table in reverse order
//  Author        : Anurag Gopal Satbhai
//  Date          : 31/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void TableRev(int iNo)
{

    if(iNo < 0)                                  // Updater
    {
        iNo = - iNo;
    }

    if(iNo == 0)
    {
        printf("Table of 0 is always 0.\n");
        return;
    }

    int iCnt = 0;

    for(iCnt = 10; iCnt >= 1; iCnt--)           // Business Logic
    {
        printf("%d\t",iCnt * iNo);
    }

    
}   // Time Complexity = O(n)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    TableRev(iValue);

    return 0;
}

