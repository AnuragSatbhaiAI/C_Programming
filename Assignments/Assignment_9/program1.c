#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : It prints pattern in combination of * and #.
//  Input         : int
//  Output        : Prints pattern
//  Author        : Anurag Gopal Satbhai
//  Date          : 31/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{

    if(iNo < 0)                                  // Updater
    {
        iNo = - iNo;
    }

    int iCnt = 0;

    for(iCnt = 1; iCnt <= iNo; iCnt++)           // Business Logic
    {
        printf("*\t");
    }

        for(iCnt = 1; iCnt <= iNo; iCnt++)           // Business Logic
    {
        printf("#\t");
    }

    
}   // Time Complexity = O(n)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    Display(iValue);

    return 0;
}

