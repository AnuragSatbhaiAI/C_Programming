#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : DollarToINR
//  Description   : It converts the dollar to Indian currency rupee (1$ = 70rupee).
//  Input         : int
//  Output        : int
//  Author        : Anurag Gopal Satbhai
//  Date          : 31/10/25
//
//////////////////////////////////////////////////////////////////////////////////////////

int DollarToINR(int iNo)
{

    int iConvert = 0;

    iConvert = 70 * iNo;

    return iConvert;

    
}   // Time Complexity = O(1)     

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0, iRet = 0;

    printf("Enter number : ");
    scanf("%d",&iValue);

    iRet = DollarToINR(iValue);

    printf("%d",iRet);

    return 0;
}

